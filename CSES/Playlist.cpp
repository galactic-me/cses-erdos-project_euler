void solve()
{
    int n;
    cin >> n;
    vi a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    int l = 0, maxe = 0;
    map<int, int> mpp;
    for (int r = 0; r < n; r++)
    {
        if (mpp.find(a[r]) != mpp.end())
        {
            l = max(l, mpp[a[r]] + 1);
        }
        mpp[a[r]] = r;
        maxe = max(maxe, r - l + 1);
    }

    cout << maxe << endl;
}

